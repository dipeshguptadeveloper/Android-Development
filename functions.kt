    val scn = Scanner(System.`in`)
    println("Enter your first number ")
    val num1 = scn.nextDouble()
    println("Enter your second number")
    val num2 = scn.nextDouble()

    fun add(num1: Double, num2: Double): Double {
        val sum = num1 + num2
        println("The Sum of $num1 + $num2 is $sum")
        return sum
    }

    fun sub(num1: Double, num2: Double): Double {
        val sub = num1 - num2
        println("The Subtraction of $num1 - $num2 is $sub")
        return sub
    }

    fun multi(num1: Double, num2: Double): Double {
        val multi = num1 * num2
        println("The Multiplication of $num1 * $num2 is $multi")
        return multi
    }

    fun div(num1: Double, num2: Double): Double {
        val div = num1 / num2
        println("The division of $num1 / $num2 is $div")
        return div
    }

    fun remainder(num1: Double, num2: Double): Double {
        val remainder = num1 % num2
        println("The remainder of $num1 % $num2 is $remainder")
        return remainder
    }

    add(num1, num2)
    sub(num1, num2)
    multi(num1, num2)
    div(num1, num2)
    remainder(num1, num2)
}
